        Object[] columnsNames={"Codigo","Nombre","Dirección","Tel fijo"};
        DefaultTableModel model= new DefaultTableModel();
        model.setColumnIdentifiers(columnsNames);
        jTable1.setModel(model);
        
        Lugares lu = new Lugares();
        List<Lugares> lista = lu.listarTodLugares();
        for(Lugares lug : lista){
            rowData[0][0]=lug.getCodLugares();
            rowData[0][1]=lug.getNombre();
            rowData[0][2]=lug.getDireccion();
            rowData[0][3]=lug.getTel_Fijo();
            model.addRow(rowData[0]);
        }
